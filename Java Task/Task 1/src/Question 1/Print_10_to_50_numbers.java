//Write a program to print the numbers from 10 to 50 using for/while loop.
public class Print_10_to_50_numbers {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 10 to 50
        System.out.println("Using for loop:");
        for (int i = 10; i <= 50; i++) {
            System.out.print(i + " ");
        }
        System.out.println(); // Move to the next line
    }
}

